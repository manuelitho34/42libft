int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*b1;
	unsigned char	*b2;

	b1 = (unsigned char *)s1;
	b2 = (unsigned char *)s2;
	i = 0;
	while (i < n && b1[i] && b2[i] && b1[i] == b2[i])
		i++;
	return (b1[i] - b2[i]);
}
